bool basic_test(bool debug = false)
{
  List<int> list;

  // insert items
  for (int i = 1; i < 10; i++)
  {
    list.insert_head(i);
  }
  cout << endl;
  cout << list;

  // 1. reverse entire linked list
  reverseList(list);
  cout << list;

  // 2. reverse first N nodes in linked list
  reverseN(list, 3);
  cout << list;

  // 3. reverse partial nodes of range [m, n] in linked list
  reverseBetween(list, 5, 8);
  cout << list;

  cout << "\n\n--------- D O N E ----------------" << endl << endl;
  return true;
}


